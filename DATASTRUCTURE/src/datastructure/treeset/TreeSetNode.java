package datastructure.treeset;

/** class TreeSetNode **/

class TreeSetNode

{

    String data;

    TreeSetNode left;
    
    TreeSetNode right;

 

    /** constructor **/

    public TreeSetNode(String data, TreeSetNode left, TreeSetNode right)

    {

        this.data  = data;

        this.left  = left;

        this.right = right;

    }    

}